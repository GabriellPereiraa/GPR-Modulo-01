                                <p class="Texto">A existência dos conflitos sociais requer, para a preservação da vida em sociedade, que os indivíduos construam regras, limites, princípios, normas, valores e objetivos que sejam o mais consensual possível. Entretanto, o consenso não acontece naturalmente, precisa ser construído.</p>

