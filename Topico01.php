                                <p class="Texto">Além da diferenciação entre os termos política (politics) e políticas públicas (polyces), Rua (2014) esclarece a respeito de dois outros termos que podem causar confusão e engano, vejamos.</p>

                                <p class="Texto">Política pública é diferente de decisão política. Observemos os exemplos dados pela autora:</p>

                                <div class="bd-callout bd-callout-success">
                                    <p class="Texto">
                                    <em>“Assim, embora uma política pública implique decisão política, nem toda decisão política chega a constituir uma política pública. Podemos encontrar um exemplo na emenda constitucional para reeleição presidencial, ou a CPMF, ou, ainda, a criação de um novo estado da federação. Observe que essas situações apresentadas são decisões, mas não são política pública. Já a reforma agrária, o sistema único de saúde, o financiamento da educação superior ou a adoção de mecanismos de transferência de renda são políticas públicas.”</em>
                                    </p>

                                    <p>– Rua, 2014</p>
                                </div>
                                <br>

                                <p class="Texto">Política pública não é a mesma coisa que atividade coletiva. Rua (2014) nos ajuda compreender com mais um exemplo: </p>

                                <div class="bd-callout bd-callout-success">
                                    <p class="Texto">
                                    <em>“O movimento pela cidadania contra a Fome e a miséria, o famoso “movimento do Betinho”, que deu ajuda a muitas famílias carentes era uma ação privada, ainda que tivesse interesse público. Já o Programa Bolsa Família é parte de uma política pública de combate à fome e à miséria. (...) a diferença entre eles é que o Programa Bolsa Família baseia-se em decisões e ações revestidas do poder extroverso e da autoridade soberana do Estado e por isso, consiste em uma política pública e não em uma ação privada de interesse público.”</em>
                                    </p>

                                    <p>– Rua, 2014</p>
