                                <p class="Texto">Buscamos assim o entendimento referente às diferenças entre política e política pública, preparando-nos para o próximo passo que será mergulhar na origem e nos conceitos, no ciclo e nas metodologias das políticas públicas. </p>
                                <hr>

                                <p class="Texto">A atenção sobre o tema política pública tem se evidenciado mais a partir das últimas décadas do século XX. Souza (2006) destaca três fatores para isso.</p>

                                <p class="SubTitulo">Fatores que chamam atenção para o tema política pública</p>

                                <p class="Texto">O primeiro deles está relacionado com as políticas restritivas de gastos, tendo em vista a mudança da compreensão do Estado intervencionista, fruto das políticas keynesianas, para a visão de um Estado mais restrito e com menos intervenções na economia e nas políticas sociais, visão está dada pela corrente conhecida como neoliberalismo.</p>

                                <p class="Texto">O segundo fator, decorrente do primeiro, está relacionado com o surgimento de uma nova visão sobre o papel dos governos, produtores, por excelência, de políticas públicas.</p>

                                <p class="Texto">O terceiro fator envolve a constatação da ineficácia dos governos de formar coalizões capazes de elaborar e implementar políticas públicas que impulsionam o desenvolvimento econômico, sem produzir exclusão social.</p>
                                <hr>

                                <p class="Texto">É nesse contexto que o campo da política pública, subárea da ciência Política, toma forma e se fortalece como área do conhecimento e disciplina acadêmica, inicialmente nos EUA como ferramenta de decisão do governo. Na figura seguinte, estão representados, segundo Souza (2006), os “pais” ou fundadores da área das políticas públicas. </p>

                                <p class="Texto">Inexiste uma conceituação precisa, única e consensual do termo política pública, tendo em vista seu caráter multidisciplinar, que envolve várias disciplinas, teorias e modelos analíticos. O que encontramos são definições dadas por vários autores e que Souza (2006) resume como sendo política pública: </p>

                                <div class="bd-callout bd-callout-success">
                                    <p class="Texto">
                                    <em>"O campo do conhecimento que busca, ao mesmo tempo, “colocar o governo em ação” e/ou analisar essa ação (variável independente) e, quando necessário, propor mudanças no rumo ou curso dessas ações (variável dependente). A formulação de políticas públicas constitui-se no estágio em que os governos democráticos traduzem seus propósitos e plataformas eleitorais em programas e ações que produzirão resultados ou mudanças no mundo real."</em>
                                    </p>

                                    <p>– Souza, 2006</p>
                                </div>

                                <p class="Texto">A mesma autora nos apresenta outras definições e seus respectivos formuladores, como exposto a seguir: </p>

                                <p class="SubSubTitulo">O que são políticas públicas?</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Um campo dentro do estudo da política que analisa o governo à luz de grandes questões públicas.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Um conjunto de ações do governo que irão produzir efeitos especificos.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">É a soma das atividades dos governos, que agem diretamente ou através de delegação e que influenciam a vida dos cidadãos.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">"O que o governo escolhe fazer ou não fazer".</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Decisões e análises sobre política pública implicam responder às seguintes questões: quem ganha o quê, por quê e que diferença faz.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="Texto">Encerrando essa parte conceitual, é importante ressaltar que a fase de implementação da política pública é seguida por seu desdobramento em planos, programas, projetos, bases de dados ou pesquisas. Muitas vezes, se torna necessária a aprovação de uma nova legislação. A partir do momento de implementação das políticas públicas, é possível executar ações de monitoramento/acompanhamento e avaliação.</p>

