                                <h2 class="Titulo" >Tópico 5 - Políticas Públicas no Brasil entre os anos 1994 a 2016</h2>
                                <br>

                                <p class="Texto">Apresenta-se a seguir as principais políticas públicas implementadas pelo governo federal entre os anos 1994 a 2016. Optou-se por este período por ser recente e pela gestão dos presidentes já terem sido encerradas e portanto, há uma possibilidade de análise sobre o desempenho das iniciativas destacadas a seguir.</p>

                                <p class="SubTitulo" id="SubTopico5.1">5.1 Políticas Públicas no Governo FHC </p>

                                <p class="Texto">O governo Fernando Henrique Cardoso estabeleceu algumas preferências para a condução e formulação da política social, reafirmando as parcerias com as organizações não governamentais (ONG’s), as Organizações da Sociedade Civil (OSC’s) e as Organizações da Sociedade Civil de Interesse Público (OSCIP's).</p>
                                <hr>

                                <p class="Texto">Dentre as políticas sociais que foram implantadas no governo FHC, destacamos algumas:</p>
                                <br>

                                <p class="SubSubTitulo">Na área da Educação</p>
                                
                                <ul>
                                    <li>
                                        <p class="Texto">Nova Lei de Diretrizes e Bases da Educação (LDB), em 1996</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Instituição do Fundo de Desenvolvimento de Ensino Fundamental (FUNDEF) para financiamento do ensino fundamental.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">primeiro Exame Nacional do Ensino Médio, em 1998</p>
                                    </li>
                                    <li>
                                        <p class="Texto">modernização dos conteúdos do ensino fundamental através dos novos Parâmetros Curriculares Nacionais (PNC);</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Bolsa-Escola em 1998</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa da Descentralização da Merenda Escolar</p>
                                    </li>
                                </ul>
                                <br>

                                <div class="card text-black border-success Texto">
                                    <div class="card-body">
                                        <p class="card-text">Importante considerarmos a focalização da atenção ao ensino fundamental e o não investimento no ensino infantil e médio, ocasionando uma considerável precarização destas áreas, bem como a descentralização na gestão dos recursos, sendo esta outra marca do governo. Segundo Draibe (2003), houve ainda uma resistência por parte dos estados e municípios que perderam recursos, uma vez que, não apresentaram um número de matrículas no ensino fundamental suficientes para garantir os repasses.</p>
                                    </div>
                                </div>
                                <br>

                                <p class="SubSubTitulo">Na política de saúde</p>
                                <p class="Texto">FHC manteve a proposta da descentralização e da focalização e investiu em algumas ações:</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Criação do Programa saúde da família e  de Agentes comunitários de saúde que tinham como foco de atenção básica à população mais pobre;</p>
                                    </li>
                                    <li>
                                        <p class="Texto">descentralização da gestão e dos gastos de acordo com as novas regras da Norma Operacional Básica de 1996.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Bolsa-Alimentação, em 2001, sendo uma estratégia no combate à mortalidade infantil.</p>
                                    </li>
                                </ul>
                                <br>

                                <div class="card text-black border-success Texto">
                                    <div class="card-body">
                                        <p class="card-text">Os investimentos em outras áreas da saúde não acompanharam o processo de descentralização da gestão e constituição da atenção básica em saúde, resultando em uma distorção das atividades preventivas que deveriam ser realizadas nas unidades de saúde, bem como, favorecendo o inchaço das unidades hospitalares.</p>
                                    </div>
                                </div>
                                <br>

                                <p class="SubSubTitulo">Na Previdência Social</p>

                                <p class="Texto">Mudanças bastante significativas foram registradas com o objetivo de atender às pressões do ajustamento fiscal. Tais alterações promovidas pela reforma previdenciária são destacadas por Draibe (2003, p.84).</p>

                                <div class="bd-callout bd-callout-success">
                                    <p class="Texto">
                                    <em>“Mudanças nos critérios de elegibilidade, pela determinação da idade mínima para as aposentadorias e a substituição dos conceitos de tempo de serviço por tempo de contribuição; mudanças nas aposentadorias, pelas alterações nas regras de cálculo, extinção das aposentadorias proporcionais e de quase todas as especiais; unificação dos regimes especiais de servidores públicos e supressão dos auxílios assistenciais (auxílio-natalidade, auxílio-funeral e renda mensal vitalícia).”</em>
                                    </p>
                                    <p class="Texto"><em>– Draibe (2003, p.84) </em></p>
                                </div>
                                <hr>

                                <p class="SubSubTitulo">Nas Políticas de emprego e proteção ao</p>

                                <ul>
                                    <li>
                                        <p class="Texto">extensão do seguro-desemprego aos empregados domésticos e a ampliação do prazo de recebimento do seguro de quatro para cinco meses, podendo chegar a sete meses.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">criação do programa Bolsa Qualificação destinado aos desempregados, que estivessem frequentando cursos de capacitação, e aos que ainda estivessem em gozo do seguro-desemprego</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Plano Nacional de Formação Profissional (PLANFOR), financiado por recursos do Fundo de Amparo ao Trabalhador (FAT) e atendendo os trabalhadores desempregados e de baixa qualificação;</p>
                                    </li>
                                    <li>
                                        <p class="Texto">programas de inserção produtiva, com destaque para os programas de microcrédito e os programas de apoio às micro e pequenas empresas.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="Texto">Compactuando com a lógica proposta pela estratégia de flexibilização e desregulamentação das relações de trabalho, para ajustá-las às novas demandas do mercado e reduzir os custos com a mão de obra, o governo FHC desenvolveu algumas alterações na legislação trabalhista, destacadas por Draibe (2003, p.86).</p>

                                <div class="bd-callout bd-callout-success">
                                    <p class="Texto">
                                    <em>“Desindexação salarial; abrangência do contrato por tempo determinado, antes restrito às atividades transitórias, sempre que resultante de negociação coletiva; instituição o banco de horas (alternativas do pagamento de horas extras); instituição da modalidade de suspensão do contrato de trabalho, por período de dois a cinco meses, associada a qualificação profissional a bolsa-qualificação; instituição do regime de trabalho em tempo parcial (com jornada de 25 horas e salário proporcional); introdução do instituto da mediação trabalhista e das comissões de Conciliação Prévia;  e reforço de mecanismos de fiscalização do trabalho.”</em>
                                    </p>
                                    <p class="Texto"><em>– Draibe (2003, p.86) </em></p>
                                </div>
                                <br>

                                <p class="SubSubTitulo">Na Assistência Social e Combate a Pobreza</p>
                                <p class="Texto">O Programa Comunidade Solidária revela de forma explícita os interesses do governo em promover o discurso da solidariedade para reafirmar o papel do Estado em relação às políticas sociais, e garantir a seletividade, a focalização. Além disso, há o direcionamento para situações de extrema pobreza com o apoio das ações humanitárias da sociedade. Destacam-se as seguintes iniciativas</p>
                                <ul>
                                    <li>
                                        <p class="Texto">Programa de Erradicação do Trabalho Infantil, em 1996</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Fundo de combate à Pobreza, em 2000</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Agente Jovem, em 2001</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Auxílio Gás, em 2002</p>
                                    </li>
                                </ul>

                                <div class="card text-black border-success Texto">
                                    <div class="card-body">
                                        <p class="card-text">Os programas de enfrentamento à pobreza seguiam os princípios da descentralização e solidariedade e eram destinados aos segmentos sociais mais pobres, focalizados pelos critérios territorial e de renda.</p>
                                    </div>
                                </div>
                                <br>

                                <p class="SubSubTitulo">No Meio Ambiente</p>
                                <ul>
                                    <li>
                                        <p class="Texto">Criação do Sistema Nacional de Unidades de Conservação da Natureza, em 2000</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Lançamento oficial da Agenda 21 em 2002</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubTitulo" id="SubTopico5.2">5.2 As políticas públicas nos governos Lula e Dilma</p>

                                <p class="Texto">Nos governos de Luiz Inácio Lula da Silva e de Dilma Rousseff,  visualizamos a garantia do controle social, não apenas nas áreas tradicionais como saúde, assistência e educação, mas com a ampliação deste mecanismo para as áreas da segurança alimentar, direitos dos idosos, política urbana, direitos das pessoas com deficiência, igualdade racial, entre outras (FAGNANI, 2011).</p>

                                <p class="Texto">As mobilizações dos grupos sociais respaldadas pelos conselhos de políticas e de direitos nas mais diversas áreas, por sua vez, proporcionaram uma reorganização institucional a partir da ampliação de ministérios e a criação de órgãos especializados e melhoria do aparato legal com destaques para a Lei de Acesso à Informação, que obriga órgãos públicos a prestarem informações sobre suas atividades a qualquer cidadão interessado e vale para todo o serviço público do país; e a Lei que estabeleceu um marco civil para a internet, sancionado pela presidente Dilma em 2014.</p>

                                <p class="Texto">Entre as políticas públicas do período destacamos as seguintes:</p>
                                <br>

                                <p class="SubSubTitulo">Segurança Alimentar e Agricultura Familiar</p>
                                <p class="Texto">A partir de 2003, é possível perceber as influências das orientações de políticas públicas voltadas a combater a fome e a miséria inicialmente com os programa Fome Zero que passou a chamar Bolsa Famĺia, que foi ampliado posteriormente com o Brasil sem Miséria.</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Programa Fome Zero</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Criação  e Consolidação do Programa Bolsa Família</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Ampliação do PRONAF (Programa de Agricultura Familiar)</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa de Aquisição de Alimentos (2003)</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Seguro da Agricultura Familiar (2004)</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Sistema único da Segurança Alimentar e Nutricional (SUSAN), em 2005.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Plano Brasil Sem Miséria, em 2011</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Luz para Todos, em 2003</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">Na Assistência Social e Direitos Humanos</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Estatuto do Idoso, aprovado em 2003.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">aprovação da nova Política de Assistência Social, em 2005</p>
                                    </li>
                                    <li>
                                        <p class="Texto">instituição do Sistema Único da Assistência Social (SUAS) em 2005</p>
                                    </li>
                                    <li>
                                        <p class="Texto">ampliação dos centros de Referência da Assistência social (CRAS)</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Redução da idade de 67 para 65 anos acesso ao Benefício de Prestação Continuada (BPC).</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Comissão Nacional da Verdade, em 2012</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Lei do Feminicídio, em 2015</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Casa da Mulher Brasileira</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">Na área da Educação</p>
                                
                                <ul>
                                    <li>
                                        <p class="Texto">Fundo de Manutenção e Desenvolvimento da Educação Básica (FUNDEB) </p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Universidade para Todos (PROUNI), em 2004.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Plano Nacional da Educação (PNE), em 2014 </p>
                                    </li>
                                    <li>
                                        <p class="Texto">Lei que destina 75% dos royalties da exploração do petróleo para a educação.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Brasil Alfabetizado e Educação de Jovens e Adultos</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Mais Educação, que visava estimular o ensino em período integral</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">Na área do Trabalho e Renda</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Primeiro Emprego, em 2003</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Nacional de Acesso ao Ensino Técnico e Emprego (Pronatec), em 2011</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">Na área da Habitação</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Sistema Nacional de Habitação, formado por subsistemas: o Sistema Nacional de Habitação de Interesse Social (SNHIS) e o Fundo Nacional de Habitação de Interesse Social.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Minha Casa, Minha Vida, que subsidia a compra de moradia popular.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">Na política de saúde</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Programa mais Saúde (PAC da Saúde), que investiu na atenção básica.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Programa Mais Médicos, em 2013.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Lei que destina 25% dos royalties da exploração do petróleo para a saúde.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">No Meio Ambiente</p>

                                <ul>
                                    <li>
                                        <p class="Texto">Lei que alterou o Código Florestal, em 2012, com objetivo de preservar a vegetação e recuperar parte do que foi desmatado. </p>
                                    </li>
                                    <li>
                                        <p class="Texto">Cadastro Ambiental Rural, a fim de permitir o controle e planejamento de áreas rurais e fornecer informações para o combate ao desmatamento.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="Texto">Os avanços das políticas públicas nos anos do governo Lula e Dilma devem ser comemorados, principalmente se comparados aos governos anteriores que não conseguiram apresentar uma proposta de continuidade e de grandes impactos iniciados no próprio desenho dos programas e projetos propostos.</p>





