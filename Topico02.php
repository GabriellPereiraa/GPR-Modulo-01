                                <p class="SubTitulo">Modelo de formulação e análise de políticas públicas</p>
                                <hr>

                                <p class="Texto">Modelos de formulação e análises de políticas públicas servem para explicar o processo de tomada de decisão. Vejamos cada um deles de acordo com Souza (2006): </p>
                                <br>

                                <p class="SubSubTitulo">1. O tipo de política pública</p>

                                <p class="Texto">Cada uma dessas políticas públicas vai gerar pontos ou grupos de vetos e de apoios diferentes, processando-se dentro do sistema político de forma também diferente. Segundo Teodor Lowi, são quatro tipos:</p>

                                <ul style="list-style-type: lower-alpha;">
                                    <li>
                                        <p class="Texto"><strong>Políticas distributivas</strong>, decisões tomadas pelo governo, que desconsideram a questão dos recursos limitados, gerando impactos mais individuais do que universais, ao privilegiar certos grupos sociais ou regiões, em detrimento do todo;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><strong>Políticas regulatórias</strong>, que são mais visíveis ao público, envolvendo burocracia, políticos e grupos de interesse;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><strong>Políticas redistributivas</strong>, que atinge maior número de pessoas e impõe perdas concretas e no curto prazo para certos grupos sociais, e ganhos incertos e futuro para outros; são, em geral, as políticas sociais universais, o sistema tributário, o sistema previdenciário e são as de mais difícil encaminhamento;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><strong>Políticas constitutivas</strong>, que lidam com procedimentos.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">2. Incrementalismo</p>

                                <p class="Texto">As políticas públicas não nascem do zero, mas de decisões marginais e incrementais que desconsideram mudanças políticas ou mudanças substantivas nos programas públicos. </p>
                                <p class="Texto">Os que trabalham nos governos e os que pesquisam os orçamentos públicos conhecem bem a força do incrementalismo, que mantém intactos estruturas governamentais e recursos para políticas públicas que deixaram de estar na agenda dos governos.</p>
                                <p class="Texto">Mas é do incrementalismo que vem a visão de que decisões tomadas no passado constrangem decisões futuras e limitam a capacidade dos governos de adotar novas políticas públicas ou de reverter a rota das políticas atuais.</p>
                                <br>

                                <p class="SubSubTitulo">3. O ciclo da política pública</p>
                                <p class="Texto">É constituído dos seguintes estágios: definição de agenda, identificação de alternativas, avaliação das opções, seleção das opções, implementação e avaliação. <strong>Será detalhado no próximo tópico.</strong></p>
                                <br>

                                <p class="SubSubTitulo">4. O modelo garbage can ou “lata de lixo”</p>
                                <p class="Texto">Esse modelo advoga que soluções procuram por problemas. As escolhas compõem uma garbage can, na qual vários tipos de problemas e soluções são colocados pelos participantes à medida que eles aparecem. Segundo este modelo, as organizações são formas anárquicas que compõem um conjunto de idéias com pouca consistência. As organizações constroem as preferências para a solução dos problemas - ação - e não, as preferências constroem a ação. A compreensão do problema e das soluções é limitada, e as organizações operam em um sistema de tentativa e erro. Em síntese, o modelo advoga que soluções procuram por problemas.</p>
                                <br>

                                <p class="SubSubTitulo">5. O modelo da coalizão de defesa</p>
                                <p class="Texto">Esse modelo discorda da visão da política pública trazida pelo ciclo da política e pelo garbage can por sua escassa capacidade explicativa sobre por que mudanças ocorrem nas políticas públicas. </p>
                                <p class="Texto">A política pública deveria ser concebida como um conjunto de subsistemas relativamente estáveis, que se articulam com os acontecimentos externos, os quais dão os parâmetros para os constrangimentos e os recursos de cada política pública. Cada subsistema que integra uma política pública é composto por um número de coalizões de defesa que se distinguem pelos seus valores, crenças e idéias e pelos recursos de que dispõem</p>
                                <br>

                                <p class="SubSubTitulo">6. Arenas sociais</p>
                                <p class="Texto">Nesse modelo a política pública é vista como uma iniciativa de Empreendedores Políticos, pessoas que mostram o problema e buscam soluções por meio de 3 mecanismos para chamar a atenção dos decisores e formuladores de políticas públicas:</p>

                                <ul style="list-style-type: lower-alpha;">
                                    <li>
                                        <p class="Texto">divulgação de indicadores que desnudam a dimensão do problema;</p>
                                    </li>
                                    <li>
                                        <p class="Texto">eventos tais como desastres ou repetição continuada do mesmo problema; e</p>
                                    </li>
                                    <li>
                                        <p class="Texto">feedback, ou informações que mostram as falhas da política atual ou seus resultados medíocres.</p>
                                    </li>
                                    <li>
                                        <p class="Texto">Esses empreendedores podem constituir, e em geral constituem, redes sociais. Redes envolvem contatos, vínculos e conexões que relacionam os agentes entre si e não se reduzem às propriedades dos agentes individuais.</p>
                                    </li>
                                </ul>
                                <br>

                                <p class="SubSubTitulo">7. O equilíbrio interrompido</p>
                                <p class="Texto">Os formuladores deste modelo acreditam que as políticas públicas surgem em momentos onde a estabilidade deu lugar à instabilidade, ou seja, momentos de crise, gerando mudanças na política anterior. Para tanto, a mídia tem um papel fundamental na construção da imagem sobre a decisão ou política pública (<i>policy image</i>).</p>
                                <p class="Texto">Os subsistemas de uma política pública permitem ao sistema político-decisório processar as questões de forma paralela, ou seja, fazendo mudanças a partir da experiência de implementação e de avaliação, e somente em períodos de instabilidade ocorre uma mudança serial mais profunda.</p>
                                <br>

                                <p class="Texto">Além dos modelos descritos acima, há ainda aqueles influenciados pelo “novo gerencialismo público” e pelo ajuste fiscal. Esses modelos evidenciam três importantes fatores:</p>
                                
                                <ul>
                                    <li>
                                        <p class="Texto">a eficiência, que é tida como objetivo principal de toda e qualquer política pública;</p>
                                        </li>
                                    <li>
                                        <p class="Texto">a credibilidade, que se baseia na existência de regras claras em contraposição à discricionariedade dos decisores públicos e burocratas, a qual levaria à inconsistência;</p>
                                        </li>
                                    <li>
                                        <p class="Texto">delegação das políticas públicas para instituições com “independência” política, justificada pela competência de seus membros e a credibilidade conquistada historicamente na atuação em diferentes mercados.</p>
                                    </li>
                                </ul>

                                <p class="Texto">Finalizando, retomamos Souza (2006), citando o que a autora extrai e sintetiza dos principais elementos das políticas públicas. </p>

