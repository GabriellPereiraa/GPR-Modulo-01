                                <h2 class="Titulo" >Tópico 3 - Ciclo de Políticas Públicas</h2>
                                <br>

                                <p class="Texto">De acordo com Rua (2014), as políticas públicas (policies) ocorrem em um ambiente tenso e de alta densidade política (politics), marcado por relações de poder,  extremamente problemáticas, entre atores do Estado e da sociedade,  entre agências intersetoriais, entre os poderes do Estado, entre o nível  nacional e níveis subnacionais, entre comunidade política e burocracia. </p>

                                <p class="Texto">Para tanto, segundo a autora, uma forma de lidar com essa complexidade, sem descartar a  dinâmica sistêmica, é associar o modelo sistêmico com o modelo do  ciclo de política (policy cycle), que aborda as políticas públicas mediante  a sua divisão em etapas sequenciais. </p>

                                <p class="Texto">A correta compreensão do ciclo das políticas pode ser de grande  valia para o gestor, favorecendo seu entendimento correto do processo  das políticas públicas e auxiliando-o a refletir com clareza sobre como  e mediante que instrumentos as políticas poderão ser aperfeiçoadas. </p>

                                <div class="card text-black border-success Texto">
                                    <div class="card-body">
                                        <p class="card-text">“Na concepção do ciclo de políticas, a política pública é considerada a resultante de uma série de atividades políticas que, agrupadas, formam o processo político.”</p>
                                        <p class="card-text">RUA, 2014</p>
                                    </div>
                                </div>

                                <div class="card text-black border-success Texto">
                                    <div class="card-body">
                                        <p class="card-text">“O Ciclo de Políticas Públicas vê a política pública como um ciclo deliberativo, formado por vários estágios e constituindo um processo dinâmico e de aprendizado. O ciclo da política pública é constituído dos seguintes estágios: definição de agenda, identificação de alternativas, avaliação das opções, seleção das opções, implementação e avaliação.”</p>
                                        <p class="card-text">SOUZA, 2006</p>
                                    </div>
                                </div>

                                <p class="Texto">O ciclo de políticas é uma abordagem para o estudo das políticas  públicas que identifica fases sequenciais e interativas-iterativas no  processo de produção de uma política. Essas fases de acordo com Rua (2014) são as seguintes:</p>

                                <ul class="fa-ul">
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> formação da agenda, que ocorre quando uma situação  qualquer é reconhecida como um problema político e  a sua discussão passa a integrar as atividades de um  grupo de autoridades dentro e fora do governo;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> formação das alternativas e tomada de decisão: ocorre  quando, após a inclusão do problema na agenda e  alguma análise deste, os atores começam a apresentar  propostas para sua resolução. Essas propostas expressam  interesses diversos, os quais devem ser combinados, de  tal maneira que se chegue a uma solução aceitável para  o maior número de partes envolvidas. Ocorre, então, a  tomada de decisão;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> a tomada de decisão não significa que todas as decisões  relativas a uma política pública foram tomadas, mas,  sim, que foi possível chegar a uma decisão sobre o  núcleo da política que está sendo formulada. Quando a  política é pouco conflituosa e agrega bastante consenso,  esse núcleo pode ser bastante abrangente, reunindo  decisões sobre diversos aspectos. Quando, ao contrário, são muitos os conflitos, as questões são demasiado  complexas ou a decisão requer grande profundidade de  conhecimentos, a decisão tende a cobrir um pequeno  número de aspectos, já que muitos deles têm as decisões  adiadas para o momento da implementação;</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> a implementação consiste em um conjunto de decisões  a respeito da operação das rotinas executivas das  diversas organizações envolvidas em uma política, de tal  maneira que as decisões inicialmente tomadas deixam  de ser apenas intenções e passam a ser intervenção  na realidade. Normalmente, a implementação se faz  acompanhar do monitoramento: um conjunto de  procedimentos de apreciação dos processos adotados,  dos resultados preliminares e intermediários obtidos  e do comportamento do ambiente da política.  O monitoramento é um instrumento de gestão das  políticas públicas e o seu objetivo é facilitar a consecução  dos objetivos pretendidos com a política; e</p>
                                    </li>
                                    <li>
                                        <p class="Texto"><span class="fa-li"></span><i class="fas fa-dot-circle CorVerdeClaroEstado"></i> a avaliação é um conjunto de procedimentos de  julgamento dos resultados de uma política, segundo  critérios que expressam valores. Juntamente com o  monitoramento, destina-se a subsidiar as decisões dos  gestores da política quanto aos ajustes necessários para  que os resultados esperados sejam obtidos.</p>
                                    </li>
                                </ul>
                                


